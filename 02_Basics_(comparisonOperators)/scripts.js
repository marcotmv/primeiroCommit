// Comparando dois números em tipo 
function iguais(a, b) {
  return a === b;
}
console.log(iguais(2,7));
console.log(iguais(3,3));
console.log(iguais(1,'1'));
console.log(iguais('10','10'));

/*A comparação nesse caso deve ser feita por 3 sinais de "===", sendo assim a os números serão comparados por tipo e valor*/